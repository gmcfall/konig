package io.konig.schemagen.plantuml;

public class PlantumlGeneratorException extends Exception {
	private static final long serialVersionUID = 1L;

	public PlantumlGeneratorException() {
	}

	public PlantumlGeneratorException(String arg0) {
		super(arg0);
	}

	public PlantumlGeneratorException(Throwable arg0) {
		super(arg0);
	}

	public PlantumlGeneratorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
