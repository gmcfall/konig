package io.konig.gae.datastore;

public class CodeGeneratorException extends Exception {
	private static final long serialVersionUID = 1L;

	public CodeGeneratorException() {
	}

	public CodeGeneratorException(String arg0) {
		super(arg0);
	}

	public CodeGeneratorException(Throwable arg0) {
		super(arg0);
	}

	public CodeGeneratorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}


}
