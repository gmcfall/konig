package io.konig.transform;

public class ShapeTransformException extends Exception {
	private static final long serialVersionUID = 1L;

	public ShapeTransformException(String arg0) {
		super(arg0);
	}

	public ShapeTransformException(Throwable arg0) {
		super(arg0);
	}

	public ShapeTransformException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
