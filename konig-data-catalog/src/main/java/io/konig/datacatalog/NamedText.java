package io.konig.datacatalog;

public class NamedText {

	private String name;
	private String text;
	
	public NamedText(String name, String text) {
		this.name = name;
		this.text = text;
	}

	public String getName() {
		return name;
	}

	public String getText() {
		return text;
	}
	
}
