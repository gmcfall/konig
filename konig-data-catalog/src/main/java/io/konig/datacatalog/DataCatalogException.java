package io.konig.datacatalog;

public class DataCatalogException extends Exception {
	private static final long serialVersionUID = 1L;

	public DataCatalogException(String arg0) {
		super(arg0);
	}

	public DataCatalogException(Throwable arg0) {
		super(arg0);
	}

	public DataCatalogException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
